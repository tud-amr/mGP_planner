function [] = plot_metrics(metrics)
% Plots logged informative metrics.

do_plot = 1;

text_size = 10.5;

time_vector = 0:5:metrics.times(end);

times = metrics.times;
P_traces = metrics.P_traces;
rmses = metrics.rmses;
wrmses = metrics.wrmses;
mlls = metrics.mlls;
wmlls = metrics.wmlls;

ts = timeseries(P_traces, times);
ts_resampled = resample(ts, time_vector, 'zoh');
P_traces_resampled = ts_resampled.data';

ts = timeseries(rmses, times);
ts_resampled = resample(ts, time_vector, 'zoh');
rmses_resampled = ts_resampled.data';

ts = timeseries(wrmses, times);
ts_resampled = resample(ts, time_vector, 'zoh');
wrmses_resampled = ts_resampled.data';

ts = timeseries(mlls, times);
ts_resampled = resample(ts, time_vector, 'zoh');
mlls_resampled = ts_resampled.data';

ts = timeseries(wmlls, times);
ts_resampled = resample(ts, time_vector, 'zoh');
wmlls_resampled = ts_resampled.data';

if (do_plot)

    % Trace of P
    subplot(2,3,1)
    
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'off'      , ...
        'YTickMode'   , 'auto'  , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YScale'      , 'log'     , ...
        'YGrid'       , 'on'      , ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size , ...
        'LooseInset'  , max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, P_traces_resampled)
    axis([0 time_vector(end) 0 1E4])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('Trace of P');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % RMSE
    subplot(2,3,2)
    
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:0.05:0.2, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, rmses_resampled)
    axis([0 time_vector(end) 0 0.5])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('RMSE');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off

    % WRMSE
    subplot(2,3,3)
    
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , 0:0.05:0.2, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, wrmses_resampled)
    axis([0 time_vector(end) 0 0.3])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('WRMSE');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off

    % MLL
    subplot(2,3,4)
    
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , -2:1, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, mlls_resampled)
    axis([0 time_vector(end) -2 1])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('MLL');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    % WMLL
    subplot(2,3,5)
    
    hold on
    set(gca, ...
        'Box'         , 'off'     , ...
        'TickDir'     , 'out'     , ...
        'TickLength'  , [.02 .02] , ...
        'XMinorTick'  , 'on'      , ...
        'YMinorTick'  , 'on'      , ...
        'YGrid'       , 'on'      , ...
        'XColor'      , [.3 .3 .3], ...
        'YColor'      , [.3 .3 .3], ...
        'YTick'       , -2:1, ...
        'LineWidth'   , 1         , ...
        'FontSize'    , text_size, ...
        'LooseInset', max(get(gca,'TightInset'), 0.02));
    
    plot(time_vector, wmlls_resampled)
    axis([0 time_vector(end) -2 1])
    h_xlabel = xlabel('Time (s)');
    h_ylabel = ylabel('WMLL');
    set([h_xlabel, h_ylabel], ...
        'FontName'   , 'Helvetica');
    hold off
    
    set(gcf, 'Position', [3173, 641, 688, 364]);
    
end

end

